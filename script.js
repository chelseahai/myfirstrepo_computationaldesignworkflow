// script.js: This file adds interactivity to the website

// Wait until the entire page is loaded
window.onload = function() {
    // Get the button element using its ID
    const button = document.getElementById('clickButton');

    // Get the paragraph element where the message will be displayed
    const message = document.getElementById('message');

    // Add a click event to the button
    button.addEventListener('click', function() {
        // When the button is clicked, show a message
        message.textContent = 'You clicked the button!';
    });
};
